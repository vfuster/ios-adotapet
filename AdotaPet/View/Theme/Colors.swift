import UIKit

extension UIColor {

    func getYellow() -> UIColor {
        return UIColor(red: 255 / 255, green: 223 / 255, blue: 83 / 225, alpha: 1)
    }
    
    func getGray() -> UIColor {
        return UIColor(red: 247 / 255, green: 247 / 255, blue: 247 / 255, alpha: 1)
    }

}
